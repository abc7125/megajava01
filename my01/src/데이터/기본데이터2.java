package 데이터;

public class 기본데이터2 {

	public static void main(String[] args) {
		//기본 데이터 저장 
		//정수,실수 저장
		
		int x=250; //int형을 쓰면 -21억~21억까지 쓸수있음.
		double y=5.5; //소수점을 쓸때는 double를 사용
		char z='한'; //한글자 문자인경우 '' 사용 
		boolean p=true; //false(반댓말)
		
		System.out.println("x: "+x); //설명을 붙일때는 " "(큰따옴표)+ 붙이기
		System.out.println("y: "+y);
		System.out.println("z: "+z);
		System.out.println("p: "+p);

	}

}
